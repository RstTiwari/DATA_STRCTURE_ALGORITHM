public class BitsManupulation {
    public static void main(String[] args) {
        int a = 19;
        int b = 3;
        System.out.println("a +b: = " + (a & b));
        System.out.println("a +b: = " + (a | b));
        System.out.println(a << 3 );
        System.out.println( a  >> 1);
        System.out.println( 21  >> 1);



    }
}
